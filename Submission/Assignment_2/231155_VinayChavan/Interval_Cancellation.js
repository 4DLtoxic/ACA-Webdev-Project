/**
 * @param {Function} fn
 * @param {Array} args
 * @param {number} t
 * @return {Function}
 */
var cancellable = function(fn, args, t) {
    fn(...args);        // fn will run at t=0s
    let timerId= setInterval(()=>{fn(...args)},t); //fn will run at t s,2t s ,..etc
    let clearFunc = ()=>clearInterval(timerId);  //This will stop the setInterval function as per below given commented code this will be invoked after cancelTimeMs using setTimeout
    return clearFunc;
};

